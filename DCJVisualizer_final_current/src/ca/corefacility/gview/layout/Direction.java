package ca.corefacility.gview.layout;

/**
 * Defines a direction along the path.
 * 
 * @author Aaron Petkau
 *
 */
public enum Direction
{
	/**
	 * A direction that is increasing along the sequence.
	 */
	INCREASING,
	
	/**
	 * A direction that is decreasing along the sequence.
	 */
	DECREASING,
	
	/**
	 * Indicates that there is no direction information.
	 */
	NONE
}